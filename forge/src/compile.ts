export type CompileResult = ValidationResult & {
  outputDir?: string;
  specHash?: string;
  schemaVersion?: string;
  policyReportPath?: string;
  provenancePath?: string;
};

async function ensureDirReady(dir: string, opts: { clean: boolean }) {
  try {
    const st = await fs.stat(dir);
    if (st.isDirectory()) {
      if (!opts.clean) {
        throw new Error(`Output directory already exists: ${dir} (use --clean to overwrite)`);
      }
      await fs.rm(dir, { recursive: true, force: true });
    }
  } catch (e: any) {
    if (e?.code !== 'ENOENT') throw e;
  }
  await fs.mkdir(dir, { recursive: true });
}

async function writeFile(filePath: string, content: string) {
  await fs.mkdir(path.dirname(filePath), { recursive: true });
  await fs.writeFile(filePath, content, 'utf8');
}

function slugify(name: string) {
  return name
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

function headerComment(specHash: string, schemaVersion: string) {
  return `/**\n * GENERATED BY FORGE — DO NOT EDIT MANUALLY\n * SPEC_HASH: ${specHash}\n * SCHEMA_VERSION: ${schemaVersion}\n */\n\n`;
}

