export async function compileFromSpecFile(
  specPath: string,
  outBaseDir: string,
  opts: { clean: boolean } = { clean: false }
): Promise<CompileResult> {
  const vr = await validateSpecFile(specPath);
  if (!vr.ok) return vr;

  const raw = await fs.readFile(specPath, 'utf8');
  const spec: any = YAML.parse(raw);

  const appName = String(spec?.app?.name || 'App');
  const outDir = path.resolve(process.cwd(), outBaseDir, slugify(appName));

  const specHash = crypto.createHash('sha256').update(raw).digest('hex');
  const schemaVersion = 'v0'; // keep stable; later: derive from schema $id/versioning

  await ensureDirReady(outDir, opts);

  const backendDir = path.join(outDir, 'backend');
  const webDir = path.join(outDir, 'web');

  // Write backend
  for (const [relPath, content] of Object.entries(backendFiles(appName, { specHash, schemaVersion }))) {
    await writeFile(path.join(backendDir, relPath), content);
  }

  // Write web
  for (const [relPath, content] of Object.entries(webFiles(appName, { specHash, schemaVersion }))) {
    await writeFile(path.join(webDir, relPath), content);
  }

  // Write manifesto
  await writeFile(
    path.join(outDir, 'FORGE_GENERATED.md'),
    `# Generated by FORGE\n\n- App: ${appName}\n- Spec: ${specPath}\n- Spec SHA256: ${specHash}\n- Schema: ${schemaVersion}\n- Generated at: ${new Date().toISOString()}\n\n## Notes\n- This output is generated. Do not edit generated code manually; change the SPEC and re-compile.\n\n## Governance\n- Policy results are recorded in POLICY_REPORT.json\n`
  );

  // Write POLICY_REPORT.json (the visible governance artifact)
  const piiFields: string[] = [];
  const entities = spec?.domain?.entities || {};
  for (const [entityName, entityDef] of Object.entries<any>(entities)) {
    const fields = entityDef?.fields || {};
    for (const [fieldName, fieldDef] of Object.entries<any>(fields)) {
      if (fieldDef?.pii === true) piiFields.push(`${entityName}.${fieldName}`);
    }
  }
  const policies = spec?.security?.policies || [];
  const hasNoPiiInLogsPolicy = policies.some((p: any) => p?.name === 'no_pii_in_logs' && p?.kind === 'logging');

  // Known advisory marker (MVP): surface as governance signal (warning, not a compile blocker).
  // We record it in POLICY_REPORT.json so the CLI can print a compact summary.
  const advisories = [
    {
      id: 'GHSA-9g9p-9gw9-jx7f',
      package: 'next',
      severity: 'high',
      title: 'Next.js vulnerable to DoS via Image Optimizer remotePatterns',
      range_affected: '>=10.0.0 <15.5.10',
      recommendation: 'Upgrade next to >= 15.5.10',
      source: 'npm audit',
      url: 'https://github.com/advisories/GHSA-9g9p-9gw9-jx7f'
    }
  ] as const;

  const policyReport = makePolicyReport({
    specHash,
    schemaVersion,
    appName,
    appVersion: String(spec?.app?.version || '0.0.0'),
    enforcementMode: 'standard',
    piiFields,
    hasNoPiiInLogsPolicy,
    advisories: advisories as any,
    warnings: vr.warnings,
    deployBlockers: []
  });

  const policyReportPath = path.join(outDir, 'POLICY_REPORT.json');
  await writeFile(policyReportPath, JSON.stringify(policyReport, null, 2) + '\n');

  // Write BUILD_PROVENANCE.json (minimal, MVP)
  const provenance = {
    forge_version: '0.1.1',
    spec_path: specPath,
    spec_hash: specHash,
    schema_version: schemaVersion,
    output_dir: outDir,
    policy_report: 'POLICY_REPORT.json',
    generated_at: new Date().toISOString()
  };
  const provenancePath = path.join(outDir, 'BUILD_PROVENANCE.json');
  await writeFile(provenancePath, JSON.stringify(provenance, null, 2) + '\n');

  return {
    ok: true,
    errors: [],
    warnings: vr.warnings,
    outputDir: outDir,
    specHash,
    schemaVersion,
    policyReportPath,
    provenancePath
  };
}
